public class Swap {
    public static void main(String[] args) {
        int a , b , t ;
        a = 10;
        b = 20;
        t = 0;

        t=a;
        a=b;
        b=t;
        System.out.println (" value of a " +a);
        System.out.println (" value of b " +b);
        System.out.println (" value of t " +t);
        System.out.println (" program ended ");

    }
}
